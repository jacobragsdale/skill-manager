fn valid_commit_sha(commit_sha: &str) -> bool {
    commit_sha.len() == 40
        && commit_sha
            .bytes()
            .all(|byte| byte.is_ascii_digit() || (b'a'..=b'f').contains(&byte))
}

fn catalog_archive_url(commit_sha: &str) -> Result<String, String> {
    if !valid_commit_sha(commit_sha) {
        return Err("GitHub returned an invalid skillbook commit SHA.".to_string());
    }

    Ok(format!("{CATALOG_SOURCE}/archive/{commit_sha}.tar.gz"))
}

fn read_catalog_metadata(catalog: &Path) -> Option<CatalogMetadata> {
    let contents = fs::read(catalog_metadata_path(catalog)).ok()?;
    let metadata = serde_json::from_slice::<CatalogMetadata>(&contents).ok()?;

    if metadata.version == CATALOG_METADATA_VERSION
        && metadata.source == CATALOG_SOURCE
        && valid_commit_sha(&metadata.commit_sha)
        && metadata
            .etag
            .as_ref()
            .is_none_or(|etag| etag.len() <= 1024 && HeaderValue::from_str(etag).is_ok())
    {
        Some(metadata)
    } else {
        None
    }
}

fn write_catalog_metadata(catalog: &Path, metadata: &CatalogMetadata) -> Result<(), String> {
    let mut contents = serde_json::to_vec_pretty(metadata)
        .map_err(|error| format!("Could not create the catalog metadata: {error}"))?;
    contents.push(b'\n');
    fs::write(catalog_metadata_path(catalog), contents)
        .map_err(|error| format!("Could not write the catalog metadata: {error}"))
}

fn is_windows_reserved_name(name: &str) -> bool {
    let stem = name.split('.').next().unwrap_or(name);
    let uppercase = stem.to_uppercase();

    matches!(uppercase.as_str(), "CON" | "PRN" | "AUX" | "NUL")
        || ["COM", "LPT"].iter().any(|prefix| {
            uppercase.strip_prefix(prefix).is_some_and(|suffix| {
                matches!(
                    suffix,
                    "1" | "2" | "3" | "4" | "5" | "6" | "7" | "8" | "9" | "¹" | "²" | "³"
                )
            })
        })
}

fn validate_portable_path_component(component: &OsStr, path: &Path) -> Result<(), String> {
    let name = component
        .to_str()
        .ok_or_else(|| format!("Archive path is not UTF-8: {}", path.display()))?;
    let has_windows_reserved_character = name.chars().any(|character| {
        character <= '\u{1f}'
            || matches!(
                character,
                '<' | '>' | ':' | '"' | '/' | '\\' | '|' | '?' | '*'
            )
    });
    let too_long_for_windows = name.encode_utf16().count() > 255;

    if name.ends_with([' ', '.'])
        || has_windows_reserved_character
        || is_windows_reserved_name(name)
        || too_long_for_windows
    {
        Err(format!(
            "Archive path is not portable to Windows: {}",
            path.display()
        ))
    } else {
        Ok(())
    }
}

fn validate_skill_name(name: &str) -> Result<(), String> {
    let valid = !name.is_empty()
        && name
            .bytes()
            .all(|byte| byte.is_ascii_lowercase() || byte.is_ascii_digit() || byte == b'-')
        && !name.starts_with('-')
        && !name.ends_with('-')
        && !name.contains("--")
        && !is_windows_reserved_name(name);

    if valid {
        Ok(())
    } else {
        Err(format!("Invalid skill name: {name}"))
    }
}

fn frontmatter_value(contents: &str, key: &str) -> Option<String> {
    let frontmatter = contents.strip_prefix("---\n")?.split_once("\n---")?.0;
    let prefix = format!("{key}:");

    frontmatter.lines().find_map(|line| {
        line.strip_prefix(&prefix)
            .map(str::trim)
            .map(|value| value.trim_matches(['"', '\'']).to_string())
            .filter(|value| !value.is_empty())
    })
}

fn skill_frontmatter(skill: &Path) -> Result<(String, String), String> {
    let path = skill.join("SKILL.md");
    let bytes =
        fs::read(&path).map_err(|error| format!("Could not read {}: {error}", path.display()))?;
    let contents = String::from_utf8(bytes)
        .map_err(|error| format!("{} must be valid UTF-8: {error}", path.display()))?;
    let normalized = contents
        .strip_prefix('\u{feff}')
        .unwrap_or(&contents)
        .replace("\r\n", "\n");
    let name = frontmatter_value(&normalized, "name")
        .ok_or_else(|| format!("{} is missing a name", path.display()))?;
    let description = frontmatter_value(&normalized, "description")
        .ok_or_else(|| format!("{} is missing a description", path.display()))?;

    Ok((name, description))
}

fn relative_path(root: &Path, path: &Path) -> Result<String, String> {
    let relative = path
        .strip_prefix(root)
        .map_err(|_| format!("{} is outside {}", path.display(), root.display()))?;
    let mut parts = Vec::new();

    for component in relative.components() {
        let Component::Normal(part) = component else {
            return Err(format!("{} has an invalid path", path.display()));
        };
        parts.push(
            part.to_str()
                .ok_or_else(|| format!("{} is not UTF-8", path.display()))?,
        );
    }

    Ok(parts.join("/"))
}

fn update_hash_field(hasher: &mut Sha256, value: &[u8]) {
    hasher.update((value.len() as u64).to_le_bytes());
    hasher.update(value);
}

fn hash_directory_entries(
    root: &Path,
    current: &Path,
    hasher: &mut Sha256,
    buffer: &mut [u8],
) -> Result<(), String> {
    let mut entries = fs::read_dir(current)
        .map_err(|error| format!("Could not read {}: {error}", current.display()))?
        .collect::<Result<Vec<_>, _>>()
        .map_err(|error| format!("Could not read {}: {error}", current.display()))?;
    entries.sort_by_key(fs::DirEntry::file_name);

    for entry in entries {
        if current == root && entry.file_name() == OsStr::new(MARKER_FILE) {
            continue;
        }

        let path = entry.path();
        let file_type = entry
            .file_type()
            .map_err(|error| format!("Could not inspect {}: {error}", path.display()))?;
        let relative = relative_path(root, &path)?;

        if file_type.is_dir() {
            hasher.update(b"directory");
            update_hash_field(hasher, relative.as_bytes());
            hash_directory_entries(root, &path, hasher, buffer)?;
        } else if file_type.is_file() {
            hasher.update(b"file");
            update_hash_field(hasher, relative.as_bytes());
            let mut file = File::open(&path)
                .map_err(|error| format!("Could not open {}: {error}", path.display()))?;
            let size = file
                .metadata()
                .map_err(|error| format!("Could not inspect {}: {error}", path.display()))?
                .len();
            hasher.update(size.to_le_bytes());

            loop {
                let read = file
                    .read(buffer)
                    .map_err(|error| format!("Could not read {}: {error}", path.display()))?;
                if read == 0 {
                    break;
                }
                hasher.update(&buffer[..read]);
            }
        } else {
            return Err(format!(
                "{} is not a regular file or directory",
                path.display()
            ));
        }
    }

    Ok(())
}

fn directory_digest(root: &Path) -> Result<String, String> {
    if !root.is_dir() {
        return Err(format!("{} is not a directory", root.display()));
    }

    let mut hasher = Sha256::new();
    let mut buffer = vec![0_u8; 64 * 1024];
    hash_directory_entries(root, root, &mut hasher, &mut buffer)?;
    let digest = hasher.finalize();
    let mut encoded = String::with_capacity(digest.len() * 2);
    const HEX: &[u8; 16] = b"0123456789abcdef";
    for byte in digest {
        encoded.push(HEX[(byte >> 4) as usize] as char);
        encoded.push(HEX[(byte & 0x0f) as usize] as char);
    }
    Ok(encoded)
}

fn valid_digest(digest: &str) -> bool {
    digest.len() == 64
        && digest
            .bytes()
            .all(|byte| byte.is_ascii_digit() || (b'a'..=b'f').contains(&byte))
}

fn install_ownership(target: &Path) -> InstallOwnership {
    let Ok(contents) = fs::read_to_string(target.join(MARKER_FILE)) else {
        return InstallOwnership::Unmanaged;
    };
    let normalized = contents
        .strip_prefix('\u{feff}')
        .unwrap_or(&contents)
        .replace("\r\n", "\n");

    if normalized == LEGACY_MARKER_CONTENTS {
        return InstallOwnership::Legacy;
    }

    match serde_json::from_str::<InstallMarker>(&normalized) {
        Ok(marker)
            if marker.version == MARKER_VERSION
                && marker.source == CATALOG_SOURCE
                && valid_digest(&marker.skill_digest) =>
        {
            InstallOwnership::Managed(marker)
        }
        _ => InstallOwnership::Unmanaged,
    }
}

fn path_entry_exists(path: &Path) -> bool {
    fs::symlink_metadata(path).is_ok()
}

fn unmanaged_status(target: &Path, catalog_digest: Option<&str>) -> SkillStatus {
    let is_real_directory =
        fs::symlink_metadata(target).is_ok_and(|metadata| metadata.file_type().is_dir());
    if !is_real_directory {
        return SkillStatus::Conflict;
    }

    match (catalog_digest, directory_digest(target)) {
        (Some(catalog_digest), Ok(installed_digest)) if installed_digest == catalog_digest => {
            SkillStatus::UnmanagedMatch
        }
        _ => SkillStatus::Conflict,
    }
}

fn installation_status(target: &Path, catalog_digest: Option<&str>) -> SkillStatus {
    if !path_entry_exists(target) {
        return SkillStatus::Available;
    }

    match install_ownership(target) {
        InstallOwnership::Unmanaged => unmanaged_status(target, catalog_digest),
        InstallOwnership::Legacy => match catalog_digest {
            Some(_) => SkillStatus::UpdateAvailable,
            None => SkillStatus::Removed,
        },
        InstallOwnership::Managed(marker) => match directory_digest(target) {
            Ok(installed_digest) if installed_digest == marker.skill_digest => match catalog_digest
            {
                Some(digest) if digest == installed_digest => SkillStatus::Installed,
                Some(_) => SkillStatus::UpdateAvailable,
                None => SkillStatus::Removed,
            },
            _ => SkillStatus::Modified,
        },
    }
}

fn catalog_skills(root: &Path) -> Result<BTreeMap<String, CatalogSkill>, String> {
    let entries = fs::read_dir(root)
        .map_err(|error| format!("Could not read catalog {}: {error}", root.display()))?;
    let mut skills = BTreeMap::new();

    for entry in entries {
        let entry =
            entry.map_err(|error| format!("Could not read catalog {}: {error}", root.display()))?;
        let file_type = entry
            .file_type()
            .map_err(|error| format!("Could not inspect {}: {error}", entry.path().display()))?;
        if !file_type.is_dir() {
            continue;
        }

        let name = entry
            .file_name()
            .to_str()
            .ok_or_else(|| "A catalog skill has a non-UTF-8 name.".to_string())?
            .to_string();
        validate_skill_name(&name)?;
        let path = entry.path();

        if path.join(MARKER_FILE).exists() {
            return Err(format!(
                "{} contains the reserved marker file",
                path.display()
            ));
        }

        let (declared_name, description) = skill_frontmatter(&path)?;
        if declared_name != name {
            return Err(format!(
                "{} declares the name {declared_name}, expected {name}",
                path.join("SKILL.md").display()
            ));
        }

        let digest = directory_digest(&path)?;
        skills.insert(
            name.clone(),
            CatalogSkill {
                name,
                description,
                digest,
            },
        );
    }

    if skills.is_empty() {
        return Err("The skillbook catalog does not contain any skills.".to_string());
    }

    Ok(skills)
}

fn append_removed_skills(
    root: &Path,
    catalog_skills: &BTreeMap<String, CatalogSkill>,
    skills: &mut Vec<Skill>,
) -> Result<(), String> {
    if !root.is_dir() {
        return Ok(());
    }

    for entry in
        fs::read_dir(root).map_err(|error| format!("Could not read {}: {error}", root.display()))?
    {
        let entry = entry.map_err(|error| format!("Could not read {}: {error}", root.display()))?;
        if !entry
            .file_type()
            .map_err(|error| format!("Could not inspect {}: {error}", entry.path().display()))?
            .is_dir()
        {
            continue;
        }

        let Some(name) = entry.file_name().to_str().map(str::to_string) else {
            continue;
        };
        if validate_skill_name(&name).is_err() || catalog_skills.contains_key(&name) {
            continue;
        }
        if matches!(
            install_ownership(&entry.path()),
            InstallOwnership::Unmanaged
        ) {
            continue;
        }

        skills.push(Skill {
            name,
            description: "This skill is no longer available in skillbook.".to_string(),
            status: installation_status(&entry.path(), None),
        });
    }

    Ok(())
}

fn collect_skill_state(
    home: &Path,
    catalog_skills: &BTreeMap<String, CatalogSkill>,
) -> Result<Vec<Skill>, String> {
    let root = install_root(home);
    let mut skills = catalog_skills
        .values()
        .map(|skill| Skill {
            name: skill.name.clone(),
            description: skill.description.clone(),
            status: installation_status(&root.join(&skill.name), Some(&skill.digest)),
        })
        .collect::<Vec<_>>();

    append_removed_skills(&root, catalog_skills, &mut skills)?;
    skills.sort_by(|left, right| left.name.cmp(&right.name));
    Ok(skills)
}

fn state_from_catalog(
    home: &Path,
    catalog_status: CatalogStatus,
    catalog_message: Option<String>,
    catalog_commit: Option<String>,
    checked_at_epoch_seconds: u64,
    auto_update_report: AutoUpdateReport,
    skills: Vec<Skill>,
) -> AppState {
    let root = install_root(home);

    AppState {
        install_root: root.display().to_string(),
        catalog_source: CATALOG_SOURCE,
        catalog_status,
        catalog_message,
        catalog_commit,
        checked_at_epoch_seconds,
        auto_update_report,
        skills,
    }
}

