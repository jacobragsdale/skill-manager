fn source_catalog_from_disk(
    source: SourceDefinition,
    cache_base: &Path,
    requested_status: SourceStatus,
    message: Option<String>,
    commit: Option<String>,
    checked_at_epoch_seconds: u64,
) -> SourceCatalog {
    let source_cache = source_cache_base(cache_base, &source.id);
    let catalog = catalog_dir(&source_cache);
    if !catalog.is_dir() {
        return SourceCatalog {
            state: source_state(
                &source,
                SourceStatus::Error,
                message.or_else(|| Some("No validated catalog is available yet.".to_string())),
                commit,
                checked_at_epoch_seconds,
            ),
            definition: source,
            path: None,
            skills: BTreeMap::new(),
        };
    }

    let Some(metadata) = read_catalog_metadata(&catalog, &source) else {
        return SourceCatalog {
            state: source_state(
                &source,
                SourceStatus::Error,
                Some(match message {
                    Some(message) => {
                        format!("{message} Cached catalog metadata is invalid or missing.")
                    }
                    None => "Cached catalog metadata is invalid or missing.".to_string(),
                }),
                commit,
                checked_at_epoch_seconds,
            ),
            definition: source,
            path: None,
            skills: BTreeMap::new(),
        };
    };

    match catalog_skills(&catalog) {
        Ok(skills) => {
            let stored_commit = commit.or(Some(metadata.commit_sha));
            SourceCatalog {
                state: source_state(
                    &source,
                    requested_status,
                    message,
                    stored_commit,
                    checked_at_epoch_seconds,
                ),
                definition: source,
                path: Some(catalog),
                skills,
            }
        }
        Err(error) => SourceCatalog {
            state: source_state(
                &source,
                SourceStatus::Error,
                Some(match message {
                    Some(message) => format!("{message} Cached catalog is invalid: {error}"),
                    None => format!("Cached catalog is invalid: {error}"),
                }),
                commit,
                checked_at_epoch_seconds,
            ),
            definition: source,
            path: None,
            skills: BTreeMap::new(),
        },
    }
}

fn finalize_prepared_source(
    source: SourceDefinition,
    cache_base: &Path,
    prepared: Result<PreparedCatalog, String>,
    checked_at_epoch_seconds: u64,
) -> SourceCatalog {
    let source_cache = source_cache_base(cache_base, &source.id);
    match prepared {
        Ok(PreparedCatalog::Current(commit_sha)) => source_catalog_from_disk(
            source,
            cache_base,
            SourceStatus::Fresh,
            None,
            Some(commit_sha),
            checked_at_epoch_seconds,
        ),
        Ok(PreparedCatalog::Staged { commit_sha, path }) => {
            let activation = activate_catalog(&path, &source_cache);
            if path.exists() {
                let _ = fs::remove_dir_all(&path);
            }
            match activation {
                Ok(()) => source_catalog_from_disk(
                    source,
                    cache_base,
                    SourceStatus::Fresh,
                    None,
                    Some(commit_sha),
                    checked_at_epoch_seconds,
                ),
                Err(error) => source_catalog_from_disk(
                    source,
                    cache_base,
                    SourceStatus::Cached,
                    Some(format!(
                        "Could not activate the refreshed catalog. Using the last validated copy. {error}"
                    )),
                    None,
                    checked_at_epoch_seconds,
                ),
            }
        }
        Err(error) => source_catalog_from_disk(
            source,
            cache_base,
            SourceStatus::Cached,
            Some(format!(
                "Could not refresh this source. Using the last validated copy when available. {error}"
            )),
            None,
            checked_at_epoch_seconds,
        ),
    }
}

async fn prepare_source(
    runtime: &RuntimeState,
    source: &SourceDefinition,
    cache_base: &Path,
) -> Result<PreparedCatalog, String> {
    let source_cache = source_cache_base(cache_base, &source.id);
    let catalog = catalog_dir(&source_cache);
    let source_for_metadata = source.clone();
    let current_metadata = {
        let _catalog_guard = runtime.catalog_lock.lock().await;
        run_blocking("Catalog metadata load", move || {
            Ok(read_catalog_metadata(&catalog, &source_for_metadata))
        })
        .await?
    };

    if source.is_built_in() {
        prepare_catalog_from_github(&runtime.github_client, current_metadata, source_cache).await
    } else {
        let source = source.clone();
        run_blocking("Git catalog refresh", move || {
            prepare_catalog_from_git(&source, current_metadata, &source_cache)
        })
        .await
    }
}

